// Operadores matemàticos 
// + y -
// * y /
// % y ** Mòdulo y potencia
console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);
console.log(2 ** 2);

// Operadores de asignaciòn
let a = 10;
a += 5; //a = a + 5
a -= 5; //a = a - 5
a *= 5; //a = a * 5
a /= 5; //a = a / 5
a %= 5; //a = a % 5
a **= 5; //a = a ** 5
console.log(a);

// Operadores de Comparaciòn
console.log(2 > 2);
console.log(2 < 2);
console.log(2 >= 2);
console.log(2 <= 2);
console.log(2 === 2);
console.log(2 == 2);
console.log(2 != 2);
console.log(2 !== 2);

// Operadores lògicos
console.log(true && true); //and
console.log(true || false); //or
console.log(!true); //not