// Declaraciòn de variables con let y const
// var: en desuso

// variables let: cambiaràn de valor
let age; //declaraciòn
console.log(age);
age = 23; //inicializaciòn
console.log(age);

let name = "Francisco";// declaraciòn e inicializaciòn
console.log(name);
name = "Paco";
console.log(name);

// constantes const: no cambiaràn de valor
const PI = 3.1416;
//
PI = 3.15;
console.log(PI);